First = 144
Second = 98

print("\n These are Arithmetic Operators \n")
print("First :", First)
print("Second :", Second)
print("Addition :", First + Second)
print("Subtraction :", First - Second)
print("Multiplication :", First * Second)
print("Division :", First / Second)
print("Floor Division :", First // Second)
print("Modulus :", First % Second)
print("Square :", First**2)
print("Square Root :", First **0.5)

print("\n These are Boolean Operators \n")
print("Equal?", First == Second)
print("Greater Than?", First > Second)
print("Less Than?", First < Second)
print("Not Equal?", First != Second)

